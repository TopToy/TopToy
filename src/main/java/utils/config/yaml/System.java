package utils.config.yaml;

public class System {
    private int n;
    private int f;
    private int w;
    private int testing;

    public System() {}
    public int getN() {
        return n;
    }

    public void setN(int n) {
        this.n = n;
    }

    public int getF() {
        return f;
    }

    public void setF(int f) {
        this.f = f;
    }

    public int getW() {
        return w;
    }

    public void setTesting(int testing) {
        this.testing = testing;
    }

    public void setW(int w) {
        this.w = w;
    }

    public int getTesting() {
        return testing;
    }
}
